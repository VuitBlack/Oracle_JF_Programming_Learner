package Section_1;

public class VarPrimitives_01 {
    public static void main(String[] args) {
        int iNum = -25;
        char cVal = 'B';
        boolean bVal = true;
        System.out.println("iNum = " + iNum);
        System.out.println("cVal = " + cVal);
        System.out.println("bVal = " +bVal);
    }
}